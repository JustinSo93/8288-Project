package domain;

public class Movement {

	private int id;
	private String movementName;
	
	public int getId(){
		return id;
		
	}
	
	public void setId(int id){
		this.id = id;
	}
	
	public String getMovementName(){
		return movementName;
		
	}
	
	public void setMovementName(String movementName){
		this.movementName = movementName;
	}
	
	
}
